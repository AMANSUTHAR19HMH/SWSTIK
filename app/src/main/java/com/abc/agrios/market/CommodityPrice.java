package com.abc.agrios.market;

public class CommodityPrice {
    private String state;
    private String district;
    private String market;
    private String commodity;
    private String variety;
    private String grade;
    private String arrival_date;
    private String min_price;
    private String max_price;
    private String modal_price;

    // Getters and Setters for each field

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getMarket() {
        return market;
    }

    public void setMarket(String market) {
        this.market = market;
    }

    public String getCommodity() {
        return commodity;
    }

    public void setCommodity(String commodity) {
        this.commodity = commodity;
    }

    public String getVariety() {
        return variety;
    }

    public void setVariety(String variety) {
        this.variety = variety;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getArrivalDate() {
        return arrival_date;
    }

    public void setArrivalDate(String arrival_date) {
        this.arrival_date = arrival_date;
    }

    public String getMinPrice() {
        return min_price;
    }

    public void setMinPrice(String min_price) {
        this.min_price = min_price;
    }

    public String getMaxPrice() {
        return max_price;
    }

    public void setMaxPrice(String max_price) {
        this.max_price = max_price;
    }

    public String getModalPrice() {
        return modal_price;
    }

    public void setModalPrice(String modal_price) {
        this.modal_price = modal_price;
    }

}
